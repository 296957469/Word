package com.Mycode.Util;

public class Word {
			private String sort;
			private String value;
			
			public Word() {
				super();
			}
			public String getSort() {
				return this.sort;
			}
			public void setSort(String sort) {
				this.sort = sort;
			}
			public String getValue() {
				return this.value;
			}
			public void setValue(String value) {
				this.value = value;
			}
					
}
